#include <stdio.h>
#include <stdlib.h>

#include "geo_buf.h"

static unsigned next_frame_stamp;

void geo_buf_init(struct geo_buf *buf) {
    buf->frame_stamp = ++next_frame_stamp;
}

unsigned get_cur_frame_stamp(void) {
    return next_frame_stamp;
}
